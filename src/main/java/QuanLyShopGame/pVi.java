    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        // TODO add your handling code here:
        lblTaiKhoan.setText(user.getTaiKhoan() + "");
        lblTong.setText(tongHD + "");
        float taiKhoan =0,tongDon =0,soDu = 0;
        taiKhoan = Float.parseFloat(lblTaiKhoan.getText());
        tongDon = Float.parseFloat(lblTong.getText());
        if(taiKhoan>tongDon){
            soDu = taiKhoan - tongDon;
            lblThongBao.setText("Thanh toán thành công");
            vietHoaDon();
            
            boolean gioHang = ghDao.xoaTatCa(""+user.getMaKH());
            frmGioHang.hienThiDanhSach();
            
        }else{
            lblThongBao.setText("Tài khoản không đủ, vui lòng nạp thêm");
            soDu = taiKhoan;
            
        }
        
        lblSoDu.setText(soDu+"");
        user = ndDao.layChiTiet(user.getMaKH());
        user.setTaiKhoan(soDu);
        
        boolean kq = ndDao.sua(user);
        frmCuaSoChinh.lblTaiKhoan.setText("Chào mừng " + user.getUserName() +"("+ user.getTaiKhoan()+"VND)");
    }//GEN-LAST:event_btnXacNhanActionPerformed

