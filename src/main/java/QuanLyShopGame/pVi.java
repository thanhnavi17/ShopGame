    public void vietHoaDon(){
        //Viết hoá đơn bán
        HoaDonBanDao hdDao = new HoaDonBanImpl();
        HoaDonBan objHD = new HoaDonBan();
        String maHoaDon = randomAlphanumeric(6);
        objHD.setMaHD(maHoaDon);
        objHD.setTenHD(user.getTenKH()+ " mua hang");
        Date ngay = new Date();
        objHD.setNgayBan(""+ngay);
        objHD.setTongHD(tongHD);
        objHD.setMaKH(user.getMaKH());
        boolean vietHD = hdDao.themMoi(objHD);
        
        //Viết hoá đơn bán chi tiết
        HoaDonBanCTDao ctDao = new HoaDonBanCTImpl();
        HoaDonBanCT objCT = new HoaDonBanCT();
        objCT.setMaHD(objHD.getMaHD());
        List<GioHang> lstGioHang = ghDao.timKiemGioHang(user.getMaKH());
        for(GioHang gh: lstGioHang){
            objCT.setMaSP(gh.getMaSP());
            objCT.setSoLuong(gh.getSoLuong());
            objCT.setGiaBan(gh.getGia());
            
            boolean vietCT = ctDao.themMoi(objCT);
        }
    }
    

