    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        SanPham objSP = new SanPham();
        
        //Lấy thông tin người dùng nhập trên giao diện và gán cho các thuộc tính của đối tượng
        objSP.setMaSP(txtMaSP.getText());
        objSP.setTenSP(txtTenSP.getText());
        objSP.setGia(Float.parseFloat(txtGia.getText()));
        objSP.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        objSP.setImg(img);
        objSP.setMaNPH(txtMaNPH.getText());
        objSP.setGhiChu(txtGhiChu.getText());
        String maLoai = "";
        LoaiSP objP = (LoaiSP)cboLoai.getSelectedItem();
        if(objP!=null){
            maLoai = objP.getMaLoai();
        }
        //Gán mã khoa vào thuộc tính của sinh viên
        objSP.setMaLoai(maLoai);
        boolean kq;
        if(!maSanPham.isEmpty()){
            kq = spDao.sua(objSP);
        }
        //TH thêm mới
        else {
            //Gọi hàm thêm mới sinh viên vào hệ thống
            kq = spDao.themMoi(objSP);
        }
        if (kq)//True
        {
            //Reload lại danh sách
            frmDanhSachSanPham.hienThiDanhSach(frmDanhSachSanPham.trang);
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

