    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        hienThiDanhSachLoaiSP();
        if(maSanPham.isEmpty()){
            this.setTitle("Thêm mới thông tin sản phẩm");
        }else//sửa
        {
            this.setTitle("Sửa đổi thông tin sản phẩm");
            //Hiển thị mã
            txtMaSP.setText(maSanPham);
            
            //Gọi hàm hiển thị chi tiết sinh viên
            hienThiChiTietSanPham();
        }
    }//GEN-LAST:event_formWindowOpened

    private void btnImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImgActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        filename = f.getAbsolutePath();
        ImageIcon imgIcon =new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(lbl_img.getWidth(),lbl_img.getHeight(),Image.SCALE_SMOOTH));
        lbl_img.setIcon(imgIcon);
        try {
            File image = new File(filename);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            for(int readNum; (readNum=fis.read(buf))!=-1;)
            {
                bos.write(buf,0,readNum);
            }
            img = bos.toByteArray();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnImgActionPerformed

    private void hienThiDanhSachLoaiSP()
    {
        LoaiSPDao loaiSPDao = new LoaiSPImpl();
        List<LoaiSP> lstLoai = loaiSPDao.layDanhSach();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        for(LoaiSP obj : lstLoai){
            model.addElement(obj);
        }
        cboLoai.setModel(model);
        
        cboLoai.setRenderer(new LoaiSPRender());
    }
    
    private void hienThiChiTietSanPham()
    {
        //Lấy thông tin chi tiết theo mã
        SanPham objSP = spDao.layChiTiet(maSanPham);
        
        if(objSP != null)
        {
            //hiển thị lên giao diện
            txtMaSP.setEditable(false);
            txtTenSP.setText(objSP.getTenSP());
            txtGia.setText(""+objSP.getGia());
            txtSoLuong.setText(""+objSP.getSoLuong());
            txtMaNPH.setText(""+objSP.getMaNPH());
            txtGhiChu.setText(""+objSP.getGhiChu());
            ImageIcon imgIcon =new ImageIcon(new ImageIcon(objSP.getImg()).getImage().getScaledInstance(lbl_img.getWidth(),lbl_img.getHeight(),Image.SCALE_SMOOTH));
            lbl_img.setIcon(imgIcon);
            String loaiSP = "";
            
            LoaiSPDao loaiSPDao = new LoaiSPImpl();
            LoaiSP obj = loaiSPDao.layChiTiet(objSP.getMaLoai());
            if(obj!=null){
                cboLoai.getModel().setSelectedItem(obj);
            }
            
        }
    }
